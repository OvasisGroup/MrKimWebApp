    <script>
        let map;
        let marker;
        let geocoder;

        // PNG Marker Image URL
        // Custom SVG marker
        const customMarkerSvg = {
            path: "M12 2C8.13 2 5 5.13 5 9c0 5.25 7 13 7 13s7-7.75 7-13c0-3.87-3.13-7-7-7zm0 9.5c-1.38 0-2.5-1.12-2.5-2.5S10.62 6.5 12 6.5s2.5 1.12 2.5 2.5S13.38 11.5 12 11.5z",
            fillColor: "green", // Google blue
            fillOpacity: 1,
            strokeWeight: 0,
            scale: 2,
            anchor: new google.maps.Point(12, 24),
        };

        function initMap() {
            geocoder = new google.maps.Geocoder();

            // Try to get the user's current location
            if (navigator.geolocation) {
                navigator.geolocation.getCurrentPosition(
                    (position) => {
                        const userLocation = {
                            lat: position.coords.latitude,
                            lng: position.coords.longitude,
                        };

                        // Initialize the map centered at the user's location
                        initializeMap(userLocation);
                    },
                    (error) => {
                        console.error("Error getting location:", error);
                        const fallbackLocation = { lat: 37.7749, lng: -122.4194 }; // San Francisco
                        initializeMap(fallbackLocation);
                    }
                );
            } else {
                alert("Geolocation is not supported by your browser.");
                const fallbackLocation = { lat: 37.7749, lng: -122.4194 }; // San Francisco
                initializeMap(fallbackLocation);
            }
        }

        function initializeMap(centerLocation) {
            // Initialize the map
            map = new google.maps.Map(document.getElementById("map"), {
                center: centerLocation,
                zoom: 12,
            });

            // Place a marker at the initial center location
            marker = new google.maps.Marker({
                position: centerLocation,
                map: map,
                icon: {
                    url: customMarkerSvg, // Use the PNG marker from public/images folder
                    scaledSize: new google.maps.Size(50, 50), // Set size for the marker
                },
            });

            // Add click event listener on the map
            map.addListener("click", (event) => {
                const clickedLocation = event.latLng;

                // Move the marker to the clicked location
                marker.setPosition(clickedLocation);

                // Reverse geocode to get address
                geocodeLatLng(clickedLocation);
            });

            // Initialize autocomplete
            const input = document.getElementById("location-input");
            const autocomplete = new google.maps.places.Autocomplete(input);

            // Listen for place selection
            autocomplete.addListener("place_changed", () => {
                const place = autocomplete.getPlace();
                if (!place.geometry || !place.geometry.location) {
                    alert("No details available for the selected location.");
                    return;
                }

                // Center the map on the selected place
                map.setCenter(place.geometry.location);
                map.setZoom(14);

                // Move the marker to the selected place
                marker.setPosition(place.geometry.location);
            });
        }

        // Reverse geocoding to get address from coordinates
        function geocodeLatLng(latLng) {
            geocoder.geocode({ location: latLng }, (results, status) => {
                if (status === "OK") {
                    if (results[0]) {
                        // Populate the input box with the address
                        document.getElementById("location-input").value =
                            results[0].formatted_address;
                    } else {
                        alert("No results found for this location.");
                    }
                } else {
                    alert("Geocoder failed due to: " + status);
                }
            });
        }

        // Initialize the map when the window loads
        window.onload = initMap;
    </script>



